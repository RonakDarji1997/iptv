# Android TV App Development Aliases
# Add these to your ~/.zshrc or ~/.bash_profile for easy access

# Quick build and run
alias iptv-build='./gradlew build'
alias iptv-run='./gradlew runApp'
alias iptv-install='./gradlew installDebug'
alias iptv-full='./build-and-run.sh'

# Change to project directory
alias cd-iptv='cd /Users/ronika/Desktop/iptv/android-tv-native'

# Combined: go to project and build/run
alias iptv-dev='cd /Users/ronika/Desktop/iptv/android-tv-native && ./build-and-run.sh'